    private void tableProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableProdutosMouseClicked
        // TODO add your handling code here:

        int linhaSelecionada = tableProdutos.getSelectedRow();

        if(linhaSelecionada >= 0){
            txtDescricao.setText(tableProdutos.getValueAt(linhaSelecionada, 1).toString());
            txtNumQtd.setText(tableProdutos.getValueAt(linhaSelecionada, 2).toString());
            txtNumPreco.setText(tableProdutos.getValueAt(linhaSelecionada, 3).toString());
            readJTable();
        }else{
            JOptionPane.showMessageDialog(null, "Você não selecionou nenhuma linha");
        }

    }//GEN-LAST:event_tableProdutosMouseClicked

