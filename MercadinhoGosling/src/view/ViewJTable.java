    public void readJTable(){
        
        DefaultTableModel formatarTable = (DefaultTableModel) tableProdutos.getModel();
        // Reseta a tabela
        formatarTable.setNumRows(0);
        ProdutoDAO pDAO = new ProdutoDAO();
        
        for(Produto p: pDAO.read()){
            
           formatarTable.addRow(new Object[]{
               p.getId(),
               p.getDescricao(),
               p.getQtd(),
               p.getPreco()
           });
            
        }
        
    }
    public void readJTableForDesc(String buscar){
        
        DefaultTableModel formatarTable = (DefaultTableModel) tableProdutos.getModel();
        // Reseta a tabela
        formatarTable.setNumRows(0);
        ProdutoDAO pDAO = new ProdutoDAO();
        
        for(Produto p: pDAO.readForDesc(buscar)){
            
           formatarTable.addRow(new Object[]{
               p.getId(),
               p.getDescricao(),
               p.getQtd(),
               p.getPreco()
           });
            
        }
        
    }
    private void btnExcuirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcuirActionPerformed
        // TODO add your handling code here:
        Produto p = new Produto();
        ProdutoDAO pDAO = new ProdutoDAO();
        int linhaSelecionada = tableProdutos.getSelectedRow();
        
        if(linhaSelecionada >= 0){
            p.setId((int)tableProdutos.getValueAt(linhaSelecionada, 0));
            pDAO.delete(p);
            txtDescricao.setText("");
            txtNumQtd.setText("");
            txtNumPreco.setText("");
            
            readJTable();
        }else{
            JOptionPane.showMessageDialog(null, "Você não selecionou nenhuma linha");
        }

        //        System.out.println("Linha selecionada: " + linhaSelecionada);

    }//GEN-LAST:event_btnExcuirActionPerformed

