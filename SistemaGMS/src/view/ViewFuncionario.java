    public void read(String string,int idFuncionario){
         DefaultTableModel formatarTable = (DefaultTableModel) tableAdiantamento.getModel();
        // Reseta a tabela
        formatarTable.setNumRows(0);
        AdiantamentosDAO aDAO = new AdiantamentosDAO();
        Funcionarios f = new Funcionarios();
        f.setId(Integer.parseInt(lblID.getText()));
        
        
        float soma = 0;
        for(Adiantamentos a: aDAO.read(f)){
            String dia = a.getDataAdiantamento().substring(8,10);
            String mes = a.getDataAdiantamento().substring(5,7);
            String ano = a.getDataAdiantamento().substring(0,4);
            String dataAdiantamento = dia+"/"+mes+"/"+ano;
            
            Object[] linhas = {
               a.getId(),
               dataAdiantamento,
               a.getSalAdiantado()
            };
            
           formatarTable.addRow(linhas);
           soma+=a.getSalAdiantado();
        }
        lblTotal.setText(Float.toString(soma));
        
    }
    public void read(int id){
        FuncionariosDAO fDAO = new FuncionariosDAO();
        
           for(Funcionarios f: fDAO.readForUpdate(id)){
               
              Date data = new Date();
              DateFormat dataFormatada = new SimpleDateFormat("Y");
              int anoAtual = Integer.parseInt(dataFormatada.format(data));
              txtNome.setText(f.getNome());
              txtObra.setText(f.getObra());
              String dia = f.getDatanascimento().substring(8,10);
              String mes = f.getDatanascimento().substring(5,7);
              String ano = f.getDatanascimento().substring(0,4);
              txtDataNasc.setText(dia+"/"+mes+"/"+ano);
              int anoNasc = Integer.parseInt(ano);
              int idade = anoAtual - anoNasc;
              lblIdade.setText(Integer.toString(idade));
               
           }
    }
   
